def main():
    # Input the first number
    num1 = float(input("Enter the first number: "))

    # Input the second number
    num2 = float(input("Enter the second number: "))

    # Multiply the numbers
    result = num1 * num2

    # Display the result
    print("The product is:", result)

if __name__ == "__main__":
    main()
